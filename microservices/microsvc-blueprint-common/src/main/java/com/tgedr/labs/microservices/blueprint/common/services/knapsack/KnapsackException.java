package com.tgedr.labs.microservices.blueprint.common.services.knapsack;

public class KnapsackException extends Exception {

	public KnapsackException() {
		super();
	}

	public KnapsackException(String arg0) {
		super(arg0);
	}

	public KnapsackException(Throwable arg0) {
		super(arg0);
	}

	public KnapsackException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
